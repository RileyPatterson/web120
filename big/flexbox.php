       <h2 class="pageID">Flexbox</h2>
     <p>Flexbox is a set of CSS tools used to control the appearance of items within a container tag based at varying display sizes. The Flexbox Layout Module was designed with the intention of making website layouts easier and more efficient because it allows the developer to set general rules for how to handle common breakpoints instead of writing and maintaining numerous media queries that could shift as content is added or edited.</p>

     <p>Unlike regular layouts consisting of block or inline elements and relying heavily on floats; flexbox allows you to align and and distribute space among items inside a flex container, even if you don’t know their size. This makes it highly suitable for responsive layouts. It also solves problems and headaches that programers often have, for example true centering. Many developers greatly appreciate the how easy Flexbox has made it to center objects (even when they overflow the flex container itself), and position child elements. The overall effect being more increasingly complex layouts achieved with cleaner, simpler code.</p>

     <p>Unfortunately, Flexbox in its current syntax is not fully supported by browsers predating 2013. Older, outdated browsers may require vender prefixing, however many developers save time and energy by running their flexbox code through the autoprefixer available at <a href="https://github.com/postcss/autoprefixer" target="_blank">https://github.com/postcss/autoprefixer</a>. Others who prefer to write out the code out themselves may find <a href="https://css-tricks.com/using-flexbox/" target="_blank"> CSS-Tricks’</a> article on how to apply the different syntax helpful. Thankfully, the digital landscape is ever evolving and these work-arounds could be rendered obsolete any day!</p>
     
