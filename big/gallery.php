       <h2 class="pageID">Galleries and Sliders</h2>
     
     <p>Many of us remember the childhood struggle of reading our first ‘adult’ book, which replaced the pictures with daunting, unbroken blocks of text. Similarly, the use of Galleries - or Sliders - in web designs are perfect tools for displaying a range of visual media, while simultaneously increasing the aesthetic appeal of the page. Galleries are especially indispensable for sites with a visual focus, such as those focused on shopping, hospitality, photography, etc.</p> 
     
     <p>There are many options available, ranging from simple, grid-based designs, to beautiful slideshows, including a plethora of free options! The needs and desires of the client will be the primary factor in deciding which gallery to use. However, the resources and capabilities of the web developer must also be taken into consideration when making the decision.</p>

     
     <p>One of the unique challenges of implementing galleries is in making them work well visually for both desktop and mobile platforms. The majority use Jquery or JavaScript, but luckily the ubiquity of web galleries has fostered a large community of developers designing any number of solutions and workarounds, many of which don't require fluency in JQuery or JS to implement. Of course, it is also possible to build a Gallery or Slider without JS, such as this great option built in CSS; <a href="https://www.cssslider.com" target="_blank">CSS Slider</a>. Just remember, when a picture is worth a thousand words, there is no need to limit yourself to your own individual capabilities, when the developer community has already, no doubt invented the wheel. 
</p>
     
