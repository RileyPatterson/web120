       <h2 class="pageID">Responsive Site VS Mobile App</h2>
     <p>     You may hear the terms mobile and responsive used interchangeably when developing web content with mobile users in mind, however the two are not synonymous. A mobile site is essentially a smaller, easier to navigate copy of a  website designed for mobile devices. They are hosted on a seperate domain - commonly m.yourdomain.com - which is also managed separately. This option can be cheaper than redesigning an existing website to be responsive.  However, in the long run, managing two separate websites and staying up to date with evolving mobile devices and browsers, it could end up costing more. </p> 
     <p>Increasingly, developers are embracing the benefits of responsive sites. A responsive website is one which adjusts accordingly to the device’s screen size and orientation. Ideally, with a responsive website, the developer is designing with a ‘mobile first’ mindset, optimizing the site for mobile users and then adding options and styles for larger screens. While the upfront cost of completely redesign a non-responsive website could inspire sticker shock in some, responsive sites are showing themselves to be excellent investments for any company looking to stay relevant and accessible in today’s digital landscape.</p>
