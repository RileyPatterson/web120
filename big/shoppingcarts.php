       <h2 class="pageID">Shopping Carts</h2>
     <p>In today's world three things are guaranteed; death, taxes, and the inevitable monetization of any new discovery or invention. The internet is no exception. A few decades ago most of us easily navigating a world before the internet and ‘online shopping,’ however, in just less than thirty years e-commerce has become ubiquitous within our modern lives, revolutionizing the modern retail environment. In many ways, the internet has served to level the playing field for entrepreneurs looking to market and sell their products and/or services. E-commerce is a powerful tool for any business, allowing access to a global marketplace!</p>

     <p>One of the most commonly used tools for e-commerce is the online shopping cart: a software package that accepts customer payment and shipping information and facilitates the distribution of that information to merchants and payment processors. Some of the more sophisticated e-commerce carts are also able to store product data, provide a portal for catalog, order and customer maintenance, and render product data, categories and website information for display to site visitors.</p>

     <p>Unfortunately, It is not just the vendors and merchants looking to make money via e-commerce, but also those looking to make money off the transfer of sensitive financial information online through exploitative, less-than-legal means. Due to the ever-evolving landscape of digital security and fraud, it is most often recommended that an online business use an outside service for handling these transactions - Secure Shopping Cart - to decrease costs and liability.</p>
     
     <p>It is important to remember that not all e-commerce service are equal; it is important any business weigh the costs, reliability, and flexibility of a service against the businesses needs and limitations.</p>
     
